package Javaoperator;

public class ArthmeticOperator {
public static void main(String[] args) {
	int a=1;
	int b=2;
	int c=a+b;
	System.out.println(c);//3
	//sub
	int d=a-b;
	System.out.println(d);//-1
	//mul
	int e=a*b;
	System.out.println(e);//2
	//div
	int f=b/a;
	System.out.println(f);//2
	//rem
	int g=b%a;
	System.out.println(g);//0
}
}
